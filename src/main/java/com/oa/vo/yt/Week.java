package com.oa.vo.yt;

public class Week {
	 private String mon;
	 private String tues;
	 private String wed;
	 private String thur;
	 private String fri;
	 private String sat;
	 private String sun;
	public String getMon() {
		return mon;
	}
	public void setMon(String mon) {
		this.mon = mon;
	}
	public String getTues() {
		return tues;
	}
	public void setTues(String tues) {
		this.tues = tues;
	}
	public String getWed() {
		return wed;
	}
	public void setWed(String wed) {
		this.wed = wed;
	}
	public String getThur() {
		return thur;
	}
	public void setThur(String thur) {
		this.thur = thur;
	}
	public String getFri() {
		return fri;
	}
	public void setFri(String fri) {
		this.fri = fri;
	}
	public String getSat() {
		return sat;
	}
	public void setSat(String sat) {
		this.sat = sat;
	}
	public String getSun() {
		return sun;
	}
	public void setSun(String sun) {
		this.sun = sun;
	}
	public Week() {
		super();
		// TODO Auto-generated constructor stub
	}
	public Week(String mon, String tues, String wed, String thur, String fri, String sat, String sun) {
		super();
		this.mon = mon;
		this.tues = tues;
		this.wed = wed;
		this.thur = thur;
		this.fri = fri;
		this.sat = sat;
		this.sun = sun;
	}
	 
	

}
